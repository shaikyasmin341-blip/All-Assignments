# Write a Python function that returns the sum of squares of numbers in a list.
def sum_of_squares(numbers):
    return sum(num ** 2 for num in numbers)

# Example usage
numbers = [1, 2, 3, 4, 5]
result = sum_of_squares(numbers)
print(f"Sum of squares: {result}")
